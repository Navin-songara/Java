public class shortrange {
    public static void main(String[] args) {
        System.out.println("Short Range");
        System.out.println("--------------");
        short b = 2;
        System.out.println("Example short value " + b);
        System.out.println("Minimum short value " + Short.MIN_VALUE); // -32768 (16 bits)
        System.out.println("Maximum short value " + Short.MAX_VALUE); // 32767
        System.out.println();

    }
}